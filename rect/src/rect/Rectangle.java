package rect;

public class Rectangle {
	private int la,lo;
	public Rectangle(int la,int lo) {
		this.la=la;
		this.lo=lo;
	}
	public int getLa() {
		return la;
	}
	public void setLa(int la) {
		this.la = la;
	}
	public int getLo() {
		return lo;
	}
	public void setLo(int lo) {
		this.lo = lo;
	}

}
